package matrix;

public class Vertex {
	
	private int weight;
	private Vertex next;
	private int vertex;
	
	public int getWeight() {
		return weight;
	}
	public void setWeight(int weight) {
		this.weight = weight;
	}
	public Vertex getNext() {
		return next;
	}
	public void setNext(Vertex next) {
		this.next = next;
	}
	public int getVertex() {
		return vertex;
	}
	public void setVertex(int vertex) {
		this.vertex = vertex;
	}

}